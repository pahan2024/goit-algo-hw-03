import re


def normalize_phone(phone_number):
    # 1. Видаляємо всі символи, крім цифр та знака '+'
    # Використовуємо регулярний вираз для очищення номера від "зайвого"
    sanitized = re.sub(r"[^\d+]", "", phone_number)

    # 2. Логіка нормалізації префікса для України
    if sanitized.startswith("380"):
        # Якщо номер починається з 380, додаємо тільки '+' на початок
        sanitized = "+" + sanitized
    elif not sanitized.startswith("+"):
        # Якщо номер не має коду країни (наприклад, починається з 050), додаємо '+38'
        sanitized = "+38" + sanitized

    return sanitized


# Вхідні дані з дз
raw_data = """
    +38(050)123-32-34
     0503451234
(050)8889900
38050-111-22-22
38050 111 22 11   
"""

# Крок 1: Розбиваємо текст на окремі рядки (елементи списку) за символом переносу рядка
# .strip() прибирає порожнечу на початку та в кінці всього блоку тексту
raw_list = raw_data.strip().split("\n")

# Крок 2: Очищаємо кожен номер у списку за допомогою функції
sanitized_numbers = [normalize_phone(num) for num in raw_list]

# Результат:
print("Нормалізовані номери телефонів для SMS-розсилки:", sanitized_numbers)
